    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String sql ="Insert into user_info(Student_ID,sName,Semester,Course,Branch,Year,Username,Password)"+"values(?,?,?,?,?,?,?,?)";
        try
        {
            pst=conn.prepareStatement(sql);
            pst.setString(1,studentid.getText());
            pst.setString(2,name.getText());
            pst.setString(3,(String)semester.getSelectedItem());
            pst.setString(4,course.getText());
            pst.setString(5,branch.getText());
            pst.setString(6,year.getText());
            pst.setString(7,username.getText());
            pst.setString(8,password.getText());
            pst.execute();

            JOptionPane.showMessageDialog(null ,"New user Added");
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

